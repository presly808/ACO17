package avdieiev.week3.TravelAgency;


public class Time {
    public int hours;

    public int minutes;

    public void setHours(int hours) {
        this.hours = hours;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public int getMinutes() {

        return minutes;
    }

    public int getHours() {
        return hours;
    }
}
